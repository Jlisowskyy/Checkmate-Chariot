#include "CMatrixIO.h"

#include <iostream>
#include <iomanip>

/////////////////////////////////////////////////////////////////////////////
// Dump a matrix
/////////////////////////////////////////////////////////////////////////////
void CMatrixIO::Dump(std::ostream &out)
{
 for (int i = 0; i < Size; i++)
 {
  for (int j = 0; j < Size; j++)
   out << std::setw(12) << GetValue(i, j) << ' ';
  out << '\n';
 }
 out << '\n';
}
