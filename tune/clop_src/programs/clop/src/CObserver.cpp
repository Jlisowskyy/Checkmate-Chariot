#include "CObserver.h"
#include "CResults.h"

/////////////////////////////////////////////////////////////////////////////
// Constructor
/////////////////////////////////////////////////////////////////////////////
CObserver::CObserver(CResults &results): results(results)
{
 results.AddObserver(*this);
}

/////////////////////////////////////////////////////////////////////////////
// Destructor
/////////////////////////////////////////////////////////////////////////////
CObserver::~CObserver()
{
 results.RemoveObserver(*this);
}
